import { KNOWLEDGE_BASE, DEBT_CONSOLIDATION_RULES } from "./knowledge-base";

export function getScriptGeneratePrompt(
  originalScript: string,
  persona: string,
  appealType: string,
  variant: "b_plan" | "new_plan" | "similar",
  analysisResult?: string
) {
  // 債務整理案件かどうかを判定
  const isDebt = /債務|借金|減額|返済|サイム/.test(originalScript + appealType);

  const variantInstructions = {
    b_plan: `## タスク: B案生成
元台本の構成・訴求を維持しつつ、表現・フック・CTAを改善したB案を作成してください。
- 元台本の良い点は残す
- 弱い部分を強化する（特にフック・感情表現・CTA）
- 同じペルソナ・訴求でより刺さる表現に
- フックは元台本と**別のパターン**を使うこと（ナレッジベースの10種から選択）`,

    new_plan: `## タスク: 新案生成
同じペルソナ・訴求で、構成を完全に刷新した新しい台本を作成してください。
- 元台本とは異なるフォーマット（元がハイテンポ型なら→フロー型、逆も可）
- フック・構成・CTAを全て新規設計
- ナレッジベースの勝ちパターンを意識して構成
- 元台本の分析結果があれば、弱点を克服する構成に`,

    similar: `## タスク: 類似台本生成
元台本のパターンを踏襲しつつ、別の切り口で類似台本を作成してください。
- 同じフォーマット（ハイテンポ型/フロー型）を維持
- 具体例・エピソードを変える
- ターゲットの別の悩みに寄せる
- フックパターンは元台本と同じ型を使ってOK`,
  };

  return `あなたは獲得広告の台本ライターです。TikTok/Reels向けの短尺動画台本を作成します。

${KNOWLEDGE_BASE}

${isDebt ? DEBT_CONSOLIDATION_RULES : ""}

${variantInstructions[variant]}

## 元台本
${originalScript}

## ペルソナ
${persona || "指定なし"}

## 訴求タイプ
${appealType || "指定なし"}

${analysisResult ? `## 元台本の分析結果\n${analysisResult}` : ""}

## 台本作成ルール（厳守）
1. テロップ＝ナレーション完全一致で書く
2. 数字・オノマトペ・感情ラベルを1本に3回以上入れる
3. CTAは必ず2〜3秒に圧縮
4. 伸びないパターン（ノウハウ型、自虐だけ、テロップ詰まりすぎ等）を避ける
5. フックは1〜2文で完結。数字・断言・否定・矛盾を冒頭に
6. 口語の語尾を使う（「〜で死ぬ」「〜してさ」「〜って話」）

## 出力形式
以下の形式で台本を出力してください:

---
【フォーマット】ハイテンポ型 or フロー型
【フックパターン】使用したパターン名（10種から選択）
【想定尺】○○秒 / シーン数○○

【フック（冒頭2秒）】
（ここに冒頭のセリフ: 12-20文字）

【本文】
シーン1: （テロップ/ナレーション）
シーン2: ...
...

【CTA（締め2-3秒）】
（行動喚起のセリフ: 8-15文字）
---

台本の後に:
1. この台本の狙い・意図
2. 使用した勝ちパターン
3. 元台本からの改善ポイント
を簡潔に説明してください。`;
}
