import { KNOWLEDGE_BASE, DEBT_CONSOLIDATION_RULES } from "./knowledge-base";

export function getAnalyzePrompt(script: string, persona: string, appealType: string, lpText?: string) {
  // 債務整理案件かどうかを判定
  const isDebt = /債務|借金|減額|返済|サイム/.test(script + appealType);

  return `あなたは獲得広告の台本分析の専門家です。
以下の広告台本を分析し、「なぜバズるか」「なぜ獲得できるか」を構造化して回答してください。

${KNOWLEDGE_BASE}

${isDebt ? DEBT_CONSOLIDATION_RULES : ""}

## 入力情報

### 台本
${script}

### ペルソナ
${persona || "指定なし"}

### 訴求タイプ
${appealType || "指定なし"}

${lpText ? `### 記事LP内容\n${lpText}` : ""}

## 分析指示

上記のナレッジベースに基づいて、以下を分析してください:

### バズ要因（各1-10点）
1. **hook（冒頭の引き）**: フックパターン10種のどれに該当するか。最初の2秒でスクロールを止められるか。数字・断言・否定・矛盾が冒頭にあるか
2. **emotion（感情喚起）**: 感情ラベル連発・オノマトペ・大げさ表現が入っているか。感情のジェットコースターがあるか
3. **structure（構成力）**: ハイテンポ型/フロー型のどちらか。時間軸構造に沿っているか。シーン数・尺は適正か
4. **cta（行動喚起）**: CTAは2〜3秒に圧縮されているか。CTAパターンのどれに該当するか
5. **trend（トレンド性）**: バズの5大法則のうちいくつ満たしているか

### 獲得要因（各1-10点）
1. **painPoint（痛みの的確さ）**: ターゲットの本質的な悩みを突けているか。「自分もありえる」リアリティがあるか
2. **solution（解決策の魅力）**: 解決後の日常が具体的に描写されているか
3. **urgency（緊急性）**: 損失回避の要素があるか。今行動すべき理由があるか
4. **trust（信頼性）**: 端数の数字（94.7%等）、具体的実績が入っているか
5. **offer（オファーの強さ）**: 申込のハードルは適切か。心理障壁の除去ができているか

### 分析サマリー
- この台本のフック型: フックパターン10種のどれか明示
- フォーマット判定: ハイテンポ型 or フロー型
- 該当する勝ちパターン: 実績データの勝ちパターンとの一致度
- 伸びないパターンに該当する点: あれば指摘

総合スコアはバズ要因+獲得要因の平均（1-10）で算出してください。
改善提案はナレッジベースの勝ちパターンに基づいて具体的に3-5個挙げてください。`;
}
