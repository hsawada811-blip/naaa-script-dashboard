export function getAnalyzePrompt(script: string, persona: string, appealType: string, lpText?: string) {
  return `あなたは獲得広告の台本分析の専門家です。
以下の広告台本を分析し、「なぜバズるか」「なぜ獲得できるか」を構造化して回答してください。

## 入力情報

### 台本
${script}

### ペルソナ
${persona || "指定なし"}

### 訴求タイプ
${appealType || "指定なし"}

${lpText ? `### 記事LP内容\n${lpText}` : ""}

## 分析基準

### バズ要因（各1-10点）
1. **hook（冒頭の引き）**: 最初の3秒で視聴を止められるか
2. **emotion（感情喚起）**: 共感・驚き・怒りなどの感情を動かせるか
3. **structure（構成力）**: 起承転結・テンポ・情報の出し方
4. **cta（行動喚起）**: 次のアクションへの導線は明確か
5. **trend（トレンド性）**: 今の時流に合っているか

### 獲得要因（各1-10点）
1. **painPoint（痛みの的確さ）**: ターゲットの本質的な悩みを突けているか
2. **solution（解決策の魅力）**: 提示する解決策は魅力的か
3. **urgency（緊急性）**: 今行動すべき理由があるか
4. **trust（信頼性）**: 信じられる根拠があるか
5. **offer（オファーの強さ）**: 申込のハードルは適切か

総合スコアはバズ要因+獲得要因の平均（1-10）で算出してください。
改善提案は具体的に3-5個挙げてください。`;
}
